package com.waveapp.smcalendar.common;


/*
 * 어플공통 객체
 * (Activity Parameter로 사용)
 */
public class CommonParam {
	
	public int year;
	public int month;
	public int day;
	
	public String date;
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}


	
}
